public class Car {

  boolean isParked;
  public String parkSlot;

  public String getParkSlot() {
    return parkSlot;
  }

  public void setParkSlot(String parkSlot) {
    this.parkSlot = parkSlot;
  }

  public boolean isParked() {
    return isParked;
  }

  public void setParked(boolean parked) {
    isParked = parked;
  }


}
